void C贯穿::f接口_销毁() {
	auto &v扩展状态 = f扩展_状态();
	v扩展状态.f对象_销毁();
}
void C贯穿::f接口_计算() {
	auto &v扩展状态 = f扩展_状态();
	//消失
	v扩展状态.v消失 += c消失速度;
	if (v扩展状态.v消失 < 0) {
		v扩展状态.v消失 = 0;
	} else if (v扩展状态.v消失 >= 1) {
		f对象_销毁();
		return;
	}
	//运动
	v扩展状态.v长度 = 数学::f线性渐变<float>(v扩展状态.v长度, c长度上限, m速度.fg大小() * m游戏速度->fg速度());
	m坐标 = v扩展状态.fg实坐标();
}
