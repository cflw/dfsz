void I粒子::f接口_初始化(const S图形参数 &a参数) {
	I图形::f接口_初始化(a参数);
	f初始化_运动(a参数.m坐标, a参数.m速度);
	f初始化_生命(a参数.m寿命);
}
bool I粒子::f接口_i可销毁() const {
	return fi死亡();
}
void I粒子::f计算_运动() {
	S运动::f计算_运动(m游戏速度->fg秒());
}
void I粒子::f计算_生命() {
	S生命::f计算_生命(m游戏速度->fg秒());
}
