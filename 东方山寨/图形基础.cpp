bool I图形::f接口_i可销毁() const {
	return m标志[e结束];
}
void I图形::f对象_使用() {
	m标志[e使用] = true;
	m计数指针->f加计数();
}
void I图形::f对象_销毁() {
	m标志.reset();
	m图形缓冲->f对象_销毁();
	m计数指针->f减计数();
}
bool I图形::f对象_i使用() {
	return m标志[e使用];
}
void I图形::f动作_结束() {
	m标志[e结束] = true;
}
