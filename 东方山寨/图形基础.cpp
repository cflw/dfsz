C圆形::C圆形(float p开始, float p结束):
	m开始半径{p开始}, m结束半径{p结束} {
}
void C圆形::f接口_计算() {
	f计算_生命();
}
void C圆形::f接口_显示() const {
	auto &v画图形 = C游戏::fg图形().fg画图形();
	v画图形.fs颜色(t颜色{1, 1, 1, fg反生命百分比()});
	v画图形.f绘制圆形(t圆形(m坐标, fg当前半径()));
}
float C圆形::fg当前半径() const {
	return 数学::f插值<float>(m开始半径, m结束半径, fg生命百分比());
}
