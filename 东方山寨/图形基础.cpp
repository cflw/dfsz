//=============================================================================
// 粒子
//=============================================================================
void I粒子::f接口_初始化(const S图形参数 &a参数) {
	I图形::f接口_初始化(a参数);
	m坐标 = a参数.m坐标;
	m速度 = a参数.m速度;
	m寿命 = a参数.m寿命;
}
bool I粒子::f接口_i可销毁() const {
	return fi死亡();
}
void I粒子::f计算_运动() {
	m坐标 += m速度 * m游戏速度->fg秒();
}
void I粒子::f计算_生命() {
	m生命 += m游戏速度->fg秒();
}
float I粒子::fg生命百分比() const {
	return m生命 / m寿命;
}
float I粒子::fg反生命百分比() const {
	return 1 - fg生命百分比();
}
bool I粒子::fi死亡() const {
	return m生命 >= m寿命;
}
