std::shared_ptr<C总血条> f总血条(int a数量) {
	auto v图形工厂 = C游戏::fg资源().f工厂_图形();
	v图形工厂.m参数.m图层 = (int)E图层::e抬显;
	return v图形工厂.f产生图形<C总血条>(a数量);
}
